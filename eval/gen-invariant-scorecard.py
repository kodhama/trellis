#!/usr/bin/env python3
"""Regenerate eval/scorecards/invariants.md from the signature catalog (decision-0028:
the scorecard is a derived resource of the catalog, not a hand-maintained copy).

Run from the repo root:  python3 eval/gen-invariant-scorecard.py
"""
import re
import sys
import pathlib

root = pathlib.Path(__file__).resolve().parent.parent
cat = (root / "core/catalog/signature-catalog-v1.md").read_text()

# Entry headings are slug-only — the display codes are retired (decision-0038).
entries = re.split(r"\n- \*\*`([a-z][a-z-]*)`\*\*", cat)
out = [
    "# Invariant scorecard (auto-derived from the signature catalog)",
    "",
    "Generated by `eval/gen-invariant-scorecard.py` — do not hand-edit (decision-0028).",
    "For each invariant: the rule the agent should follow, then the specific ✗ failures to look for in a",
    "transcript. A reviewer marks each **followed / violated / n-a** with an evidence quote.",
    "",
]


def field(body, name):
    m = re.search(rf"- {name}: (.*(?:\n(?!\s*- )\s+.*)*)", body)
    return re.sub(r"\s+", " ", m.group(1)).strip() if m else ""


def violated(body):
    seg = body.split("- violated:")[1].split("- class:")[0]
    return [re.sub(r"\s+", " ", m.strip())
            for m in re.findall(r"- \*\(\w+\)\* (.+?)(?=\n\s*- |\Z)", seg, re.S)]


n = 0
for i in range(1, len(entries), 2):
    slug, body = entries[i], entries[i + 1]
    if "- directive:" not in body:
        continue
    n += 1
    out += [f"## {slug}",
            f"- **rule:** {field(body, 'directive')}",
            "- **✗ look for:**"]
    out += [f"  - {v}" for v in violated(body)]
    out.append("")

if n == 0:
    sys.exit("FATAL: parsed 0 invariants from the catalog — did its heading format change? "
             "Never write an empty scorecard silently (floor-transparency).")

(root / "eval/scorecards/invariants.md").write_text("\n".join(out))
print(f"regenerated eval/scorecards/invariants.md for {n} invariants", file=sys.stderr)
